    function initForms() {
        // Uppercase inputs
        document.querySelectorAll('input.uppercase').forEach(function(input) {
            input.addEventListener('input', function() {
                this.value = this.value.toUpperCase();
            });
        });

        // Form validation feedback
        document.querySelectorAll('form').forEach(function(form) {
            form.addEventListener('submit', function(e) {
                const invalidFields = form.querySelectorAll(':invalid');
                if (invalidFields.length > 0) {
                    invalidFields[0].focus();
                }
            });
        });

        // Auto-generate document title
        const typeSelect = document.getElementById('type');
        const monthSelect = document.getElementById('month');
        const yearSelect = document.getElementById('year');
        const titleInput = document.getElementById('title');

        if (typeSelect && monthSelect && yearSelect && titleInput) {
            function updateTitle() {
                if (!titleInput.value || titleInput.dataset.autoGenerated === 'true') {
                    const typeText = typeSelect.options[typeSelect.selectedIndex]?.text || '';
                    const monthText = monthSelect.options[monthSelect.selectedIndex]?.text || '';
                    const yearText = yearSelect.value || '';

                    if (typeText && monthText && yearText) {
                        titleInput.value = `${typeText} - ${monthText} ${yearText}`;
                        titleInput.dataset.autoGenerated = 'true';
                    }
                }
            }

            [typeSelect, monthSelect, yearSelect].forEach(function(el) {
                el.addEventListener('change', updateTitle);
            });

            titleInput.addEventListener('input', function() {
                if (this.value) {
                    this.dataset.autoGenerated = 'false';
                }
            });
        }
    }

    // ================================
    // File Upload Enhancement
    // ================================
    function initFileUpload() {
        const fileInput = document.getElementById('document');

        if (fileInput) {
            fileInput.addEventListener('change', function() {
                const file = this.files[0];
                if (file) {
                    // Validate file size
                    const maxSize = 10 * 1024 * 1024; // 10MB
                    if (file.size > maxSize) {
                        showToast('File troppo grande. Massimo 10MB', 'error');
                        this.value = '';
                        return;
                    }

                    // Validate file type
                    const allowedTypes = ['application/pdf', 'image/jpeg', 'image/png',
                                         'application/msword',
                                         'application/vnd.openxmlformats-officedocument.wordprocessingml.document'];
                    if (!allowedTypes.includes(file.type)) {
                        showToast('Tipo file non consentito', 'error');
                        this.value = '';
                        return;
                    }

                    // Show file info
                    showToast(`File selezionato: ${file.name}`, 'info');
                }
            });
        }
    }

    // ================================
    // Table Enhancements
