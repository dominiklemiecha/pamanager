    // ================================
    // Auto-save Draft (Communications)
    // ================================
    function initAutoSave() {
        const form = document.querySelector('form.form-card');
        const titleInput = document.getElementById('title');
        const contentInput = document.getElementById('content');

        if (form && titleInput && contentInput) {
            const pageKey = window.location.pathname + '_draft';

            // Load draft
            const savedDraft = localStorage.getItem(pageKey);
            if (savedDraft) {
                const draft = JSON.parse(savedDraft);
                if (!titleInput.value && draft.title) {
                    titleInput.value = draft.title;
                }
                if (!contentInput.value && draft.content) {
                    contentInput.value = draft.content;
                }
            }

            // Auto-save
            let saveTimeout;
            function saveDraft() {
                clearTimeout(saveTimeout);
                saveTimeout = setTimeout(function() {
                    const draft = {
                        title: titleInput.value,
                        content: contentInput.value,
                        timestamp: Date.now()
                    };
                    localStorage.setItem(pageKey, JSON.stringify(draft));
                }, 1000);
            }

            titleInput.addEventListener('input', saveDraft);
            contentInput.addEventListener('input', saveDraft);

            // Clear draft on submit
            form.addEventListener('submit', function() {
                localStorage.removeItem(pageKey);
            });
        }
    }

