    // ================================
    // Rich Text Editor (Basic)
    // ================================
    function initRichText() {
        const contentTextarea = document.getElementById('content');

        if (contentTextarea) {
            // Add basic formatting toolbar
            const toolbar = document.createElement('div');
            toolbar.className = 'rich-toolbar';
            toolbar.innerHTML = `
                <button type="button" data-command="bold" title="Grassetto"><b>B</b></button>
                <button type="button" data-command="italic" title="Corsivo"><i>I</i></button>
                <button type="button" data-command="list" title="Lista">Lista</button>
            `;

            contentTextarea.parentNode.insertBefore(toolbar, contentTextarea);

            toolbar.querySelectorAll('button').forEach(function(btn) {
                btn.addEventListener('click', function() {
                    const command = this.dataset.command;
                    const start = contentTextarea.selectionStart;
                    const end = contentTextarea.selectionEnd;
                    const text = contentTextarea.value;
                    const selected = text.substring(start, end);

                    let replacement = '';
                    switch (command) {
                        case 'bold':
                            replacement = `<b>${selected}</b>`;
                            break;
                        case 'italic':
                            replacement = `<i>${selected}</i>`;
                            break;
                        case 'list':
                            replacement = `\n- ${selected}`;
                            break;
                    }

                    contentTextarea.value = text.substring(0, start) + replacement + text.substring(end);
                    contentTextarea.focus();
                });
            });
        }
    }

